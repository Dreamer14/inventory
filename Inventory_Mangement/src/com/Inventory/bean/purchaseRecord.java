package com.Inventory.bean;

public class purchaseRecord {
	
	 
	   int productID;
	   String nameOfInventory;
	   String purchaseDate;
	   String versionOfInventory;
	   String amountOfInventory;
	   String vendorName;
	   String quality;
	   
	   
	public int getProductID() {
		return productID;
	}
	public void setProductID(int productID) {
		this.productID = productID;
	}
	public String getNameOfInventory() {
		return nameOfInventory;
	}
	public void setNameOfInventory(String nameOfInventory) {
		this.nameOfInventory = nameOfInventory;
	}
	public String getPurchaseDate() {
		return purchaseDate;
	}
	public void setPurchaseDate(String purchaseDate) {
		this.purchaseDate = purchaseDate;
	}
	public String getVersionOfInventory() {
		return versionOfInventory;
	}
	public void setVersionOfInventory(String versionOfInventory) {
		this.versionOfInventory = versionOfInventory;
	}
	public String getAmountOfInventory() {
		return amountOfInventory;
	}
	public void setAmountOfInventory(String amountOfInventory) {
		this.amountOfInventory = amountOfInventory;
	}
	public String getVendorName() {
		return vendorName;
	}
	public void setVendorName(String vendorName) {
		this.vendorName = vendorName;
	}
	public String getQuality() {
		return quality;
	}
	public void setQuality(String quality) {
		this.quality = quality;
	}
	
	

  }
