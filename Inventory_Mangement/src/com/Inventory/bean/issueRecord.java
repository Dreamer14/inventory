package com.Inventory.bean;

public class issueRecord {
	
	int productID;
	String nameInventory;
	String issueDate;
	String issueNoDay;
	String returnDate;
	String versionOfInventory;
	String issueToDepartment;
	
	
	public int getProductID() {
		return productID;
	}
	public void setProductID(int productID) {
		this.productID = productID;
	}
	public String getNameInventory() {
		return nameInventory;
	}
	public void setNameInventory(String nameInventory) {
		this.nameInventory = nameInventory;
	}
	public String getIssueDate() {
		return issueDate;
	}
	public void setIssueDate(String issueDate) {
		this.issueDate = issueDate;
	}
	public String getIssueNoDay() {
		return issueNoDay;
	}
	public void setIssueNoDay(String issueNoDay) {
		this.issueNoDay = issueNoDay;
	}
	public String getReturnDate() {
		return returnDate;
	}
	public void setReturnDate(String returnDate) {
		this.returnDate = returnDate;
	}
	public String getVersionOfInventory() {
		return versionOfInventory;
	}
	public void setVersionOfInventory(String versionOfInventory) {
		this.versionOfInventory = versionOfInventory;
	}
	public String getIssueToDepartment() {
		return issueToDepartment;
	}
	public void setIssueToDepartment(String issueToDepartment) {
		this.issueToDepartment = issueToDepartment;
	}
	
	

}
